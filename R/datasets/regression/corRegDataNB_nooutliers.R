RegData.DataGenerator <- function(dataConf){
    nn <- dataConf$n
    nn <- nn/10
    d  <- dataConf$d
    p  <- dataConf$p
    require("MASS")
    if(nn<=0 || d<=0){
      stop("dimension or sampling number are less than zero")
    }
    mean <- rep(0,d)
    var <- diag(d)
    emean <- rep(0,nn)
    evar <- matrix(rep(p,(nn)^2),nn)+(1-p)*diag(nn)
    coef <- rep(sqrt(3/d),d)
    x<-mvrnorm(nn,mean,var)
    print(x)
    epsilons <- matrix(mvrnorm(1,emean,evar),nn,1)
    print(epsilons)
    y <- x%*%coef+ epsilons
    return(list(x,y))
}


